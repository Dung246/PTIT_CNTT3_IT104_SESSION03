let strValue: string = "2";    // Biến string
let numValue: number = 2;      // Biếnnnumber

// So sánh lỏng 
console.log(strValue == numValue); 
// Kết quả: true
// Toán tử == chỉ so sánh giá trị, không quan tâm kiểu dữ liệu.


// So sánh chặt 
console.log(strValue === numValue); 
// Kết quả: false
// Toán tử === so sánh cả giá trị và kiểu dữ liệu.

